#include <iostream>using namespace std;
 int main(int argc, char *argv[]) {
 /* this is a c-style comment	  and it extends until the closing	  star-slash comment mark */
 cout << "Hello World!\n";
 // this c++-style comment ends at the end of the line
 cout << "That comment ended!";
 // double slash comments can be alone on a line /* as can slash-star comments */
 return 0;
}
